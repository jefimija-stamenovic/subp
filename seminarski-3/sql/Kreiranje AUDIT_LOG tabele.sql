CREATE TABLE AUDIT_LOG (
    ID_LOGA INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMP_NO SMALLINT NOT NULL,
    OPERACIJA VARCHAR(10) NOT NULL,
    KORISNIK VARCHAR(31) DEFAULT CURRENT_USER,
    VREME_PROMENE TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    STARA_PLATA NUMERIC(15,2),
    NOVA_PLATA NUMERIC(15,2),
    CONSTRAINT FK_AUDIT_EMP FOREIGN KEY (EMP_NO) REFERENCES EMPLOYEE (EMP_NO)
);
